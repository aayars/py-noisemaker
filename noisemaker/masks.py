@mask(lambda: [random.randint(7, 9), random.randint(12, 24), 1])
def script(x, y, row, shape, uv_noise, uv_y, uv_x, **kwargs):
    height = shape[0]
    width = shape[1]

    x_step = x % width
    y_step = y % height

    if x > 0 and (x + y) % 2 == 1:
        return row[x - 1]

    if y_step == 0:
        return 0

    if y_step in (1, 3, 6):
        return uv_random(uv_noise, uv_x, uv_y) > .25

    if y_step in (2, 4, 5):
        return uv_random(uv_noise, uv_x, uv_y) > .9

    if x_step == 0:
        return 0

    if any(n == 0 for n in (width - x_step, height - y_step)):
        return 0

    if all(n % 2 == 0 for n in (x_step, y_step)):
        return 0

    if y_step == height - 1:
        return 0

    return uv_random(uv_noise, uv_x, uv_y) > .5


