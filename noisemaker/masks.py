    float2 = uv_noise[(uv_y + int(shape[0] * .5)) % uv_shape[0]][uv_x]
    float3 = uv_noise[uv_y][(uv_x + int(shape[1] * .5)) % uv_shape[1]]

    if uv_fract < .5:
        _x = tex_x
        tex_x = tex_y
        tex_y = _x

    if float2 < .5:
        tex_x = shape[1] - tex_x - 1

    if float3 < .5:
        tex_y = shape[0] - tex_y - 1

    return atlas[uv_floor][tex_x][tex_y]


