def _hash(tensor):
    return hashlib.sha256(tensor.numpy().tobytes()).hexdigest()

@pytest.mark.parametrize("seed", SEEDS)
def test_basic(seed):
    rng.set_seed(seed)
    value.set_seed(seed)
    tensor = generators.basic(2, [128, 128, 3], hue_rotation=0)
    assert tensor.shape == (128, 128, 3)
    assert _hash(tensor) == EXPECTED_BASIC[seed]

@pytest.mark.parametrize("seed", SEEDS)
def test_multires(seed):
    rng.set_seed(seed)
    value.set_seed(seed)
    tensor = generators.multires(
        None,
        seed,
        freq=2,
        shape=[128, 128, 3],
        octaves=2,
        hue_rotation=0,
        post_effects=[],
        final_effects=[],
    )
    assert tensor.shape == (128, 128, 3)
    assert _hash(tensor) == EXPECTED_MULTIRES[seed]
